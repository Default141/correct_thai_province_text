# Function to correct province name
def correct_province(province):
    if province in corrected_provinces:
        return province
    else:
        # Use fuzzy string matching to find the closest match
        closest_match, score = process.extractOne(province, corrected_provinces)
        if score >= 80:  # Adjust the threshold as needed
            return closest_match
        else:
            return province  # Return the original province name if not a close match

